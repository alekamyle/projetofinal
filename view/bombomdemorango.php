        <h2>Bombom de Travessa</h2>

        <h2> Ingredientes </h2>

        <p>Creme Branco<br>
            2 xícaras (chá) de leite (480ml)<br>
            2 colheres (sopa) de amido de milho (18g)<br>
            1 caixa de leite condensado (395g)<br>
            1 caixa de creme de leite (200g)<br>
            1 colher (chá) de essência de baunilha (5ml)<br>
        </p>

        <h2> Ganache de Chocolate</h2>
        <p>300g de chocolate meio amargo<br>
           2 caixinhas de creme de leite (400g)<br>
           2 caixas de morangos<br>
          2 colheres (sopa) de creme de avelã (Nutella) (opcional)<br>
        </p>

        <h2>Preparo do Creme Branco </h2>
        <p>Na panela coloque o leite condensado e o amido de milho. Misture até que dissolva bem.<br>
            Acrescente o leite, o creme de leite e a essência de baunilha.<br>
            Misture e leve ao fogo baixo, mexendo sem parar até ganhar consistência.<br>
            Assim que começar a borbulhar, continue misturando por mais 5 minutos.<br>
            Retire do fogo, transfira para uma vasilha, cubra com plástico em contato e deixe esfriar.<br>
        </p>

        <h2>Preparo da Ganache</h2>
        <p> Derreta o chocolate em banho-maria ou no micro-ondas, de 30 em 30 segundos, misturando a cada tempo até derreter por completo.<br>
            Junte o creme de leite e misture bem. Reserve.<br>
        </p>

        <h2>Montagem </h2>
        <p>Na travessa, espalhe uma camada da ganache de chocolate formando uma base.<br>
            Depois, acomode os morangos lavados, secos e sem o cabinho, um ao lado do outro.<br>
            Sobre os morangos, espalhe todo o creme branco e o restante da ganache de chocolate.<br>
            Leve à geladeira por cerca de 2 horas.<br>
            Decore o topo da travessa com morangos cortados em 4 partes preservando o cabinho.<br>
            Coloque o creme de avelã na manga de confeitar, corte a pontinha e decore como preferir.<br>
            Sirva gelado.
            Rendimento: 15 porções.
        </p>

        <h2> Dica</h2>
        <p>Se o creme de avelã estiver bem consistente, coloque no micro-ondas por 10 segundos para que amoleça e facilite a decoração.<br>
           Validade: 5 dias armazenado na geladeira.<br>
        </p>
 


