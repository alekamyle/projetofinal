        <h2>Pudim</h2>

        <h2> Ingredientes </h2>
        <p>Calda<br>
            1 e ½ xícaras (chá) de açúcar (360g)<br>
            ½ xícara (chá) de água (120ml)<br>
        </p>

        <h2> Pudim</h2>
        <p> 5 ovos<br>
            1 litro de leite<br>
            2 xícaras (chá) de açúcar (360g)<br>
            2 colheres (sopa) de manteiga ou margarina (40g)<br>
            2 e ½ xícaras (chá) de farinha de trigo (350g)<br>
        </p>

        <h2>Preparo da Calda</h2>
        <p> Na panela coloque o açúcar e leve ao fogo baixo, mexendo sem parar até derreter.<br>
            Junte a água aos poucos e deixe em fogo baixo, até derreter os torrões de açúcar.<br>
            Retire do fogo e despeje na forma de 28cm x 18cm e reserve.<br>
        </p>

        <h2>Preparo do Pudim</h2>
        <p>No liquidificador coloque os ovos, a manteiga, o açúcar e uma parte do leite. Bata tudo muito bem.<br>
            Junte a farinha de trigo e o restante do leite.<br>
            Bata mais um pouco e despeje delicadamente na forma caramelada.<br>
            Cubra com papel alumínio, leve para assar em banho-maria em forno preaquecido a 200°C por cerca de 60 minutos.<br>
            Retire do forno, deixe amornar e desenforme.<br>
            Leve à geladeira e sirva gelado.<br>
        </p>
