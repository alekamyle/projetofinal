        <h2>Torta de Limao</h2>

        <h2> Ingredientes </h2>
        <p>200 g biscoito doce.<br>
           100 g manteiga sem sal.<br>
           1 colher chá de raspas de limão.<br>
           1 e 1/2 latas de leite condensado.<br>
           100 ml  de creme de leite (1/2 caixinha).<br>
           1/3 xícara chá de suco de limão coado.<br>
           4 claras em neve.<br>
           ½ xícara chá de açúcar.<br>
           Raspas de limão a gosto.<br>
        </p>

        <h2>Massa</h2>
        <p> Em um processador ou liquidificador adicione os biscoitos, triture bem e reserve.<br>
            Em uma tigela adicione os biscoitos triturados a manteiga e as raspas de limão.<br>
            Misture bem até formar uma farofa úmida.<br>
            Em uma forma redonda com fundo falso  no tamanho de 20 cm de diâmetro acomode a massa,<br>
            apertando para que fique bem compacta. Leve ao forno preaquecido por aproximadamente 10 a 15 minutos <br>
            ou até dourar.<br>
            Retire do forno  e reserve.<br>
        </p>

        <h2>Recheio</h2>
        <p>Em uma tigela adicione o leite condensado, o creme de leite, o suco de limão coado e raspas de limão.<br>
           Misture bem e espalhe este creme sobre a base da torta. Reserve.<br>
        </p>

        <h2>Merengue</h2>
        <p>Bata as claras em neve.<br>
           Adicione o açúcar aos poucos até formar picos firmes.<br>
           Coloque sobre a torta e leve ao forno moderado até começar a dourar ou use o maçarico.<br>
           Retire do forno e polvilhe com raspas de limão para decorar.<br>
           Leve à geladeira e sirva gelada.<br>
           Rendimento 8 porções.<br>
        </p>
