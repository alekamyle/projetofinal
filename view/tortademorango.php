    <section class="receita">
        <h2>Torta de Morango</h2>

        <h2> Base </h2>
        <p>150g de biscoito tipo maizena<br>
            100g de manteiga sem sal<br>
        </p>

        <h2> Creme branco</h2>
        <p>500ml de leite<br>
        2 colheres (sopa) de amido de milho<br>
        1 lata de leite condensado<br>
        1 caixa de creme de leite<br>
        1 gema passada pela peneira<br>
        1 colher (chá) de essência de baunilha<br>
        </p>

        <h2>Geleia de Brilho</h2>
        <p>1 xícara (chá) de água<br>
            1 envelope de gelatina em pó sabor morango<br>
            2 colheres (sopa) de açúcar<br>
            1 colher (sopa) de amido de milho<br>
        </p>

        <h2>Decoração</h2>
        <p>2 caixas de morangos<br>
            50g castanha de caju triturada<br>
    </p>

        <h2>Preparo do Creme</h2>
        <p> Na panela coloque o leite condensado e o amido de milho, misture até dissolver bem.<br>
            Junte o leite, o creme de leite, a essência de baunilha e a gema passada pela peneira.<br>
            Misture bem e leve ao fogo baixo, mexendo sem parar até ganhar consistência.<br>
            Assim que começar a borbulhar, continue misturando por mais uns 2 minutos.<br>
            Retire do fogo, transfira para outra vasilha e cubra com um plástico em contato e deixe esfriar.<br>
        </p>



        <h2>Preparo da Base </h2>
        <p>Triture os biscoitos no liquidificador ou processador até formar uma farofa fina.<br>
            Junte a manteiga em temperatura ambiente e triture mais um pouco até formar uma farofa mais úmida.<br>
            Espalhe no refratário e pressione com as costas da colher até que fique bem compacta.<br>
            Leve para assar em forno preaquecido a 180°C por cerca de 10 minutos.<br>
            Retire do forno e deixe esfriar. Reserve.<br>
        </p>

        <h2>Preparo da Geleia de Brilho </h2>
        <p>Na panela, junte a água com o amido de milho e misture até dissolver.<br>
           Acrescente o açúcar e leve em fogo baixo mexendo sempre até aquecer bem e dissolver o açúcar.<br>
           Junte a gelatina em pó sabor morango e misture mais um pouco para dar uma leve encorpada.<br>
           Retire do fogo e deixe esfriar.<br>
        </p>

        <h2>Montagem</h2>
        <p>Sobre a base de biscoitos, despeje todo o creme branco e alise bem.<br>
            Sob o creme, acomode os morangos cortados ao meio e despeje delicadamente a geleia de brilho sobre os morangos.<br>
            Leve à geladeira por cerca de 2 horas ou até ficar firme.<br>
            Retire da geladeira e decore com a castanha de caju.<br>
            Sirva à vontade.
        </p>

