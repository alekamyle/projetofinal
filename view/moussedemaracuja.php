        <h2>Receita</h2>

        <h2> Ingredientes </h2>
        <p> Mousse
            1 caixa de leite condensado<br>
            1 caixa de creme de leite<br>
            1 copo (tipo americano) de suco concentrado de maracujá<br>
            3 colheres (sopa) de leite em pó<br>
        </p><br>

        <h2>Cobertura</h2>
        <p> ½ xícara (chá) de polpa de maracujá<br>
            ¼ xícara (chá) de açúcar<br>
            Meio copo (tipo americano) de água<br>
        </p><br>

        <h2>Preparo do Mousse </h2>
        <p> No liquidificador coloque o:<br>
            leite condensado;<br>
            o creme de leite;<br>
            o suco concentrado de maracujá;<br>
            o leite em pó;<br>
            Bata bem.<br>
            Despeje no refratário e leve à geladeira por cerca de 2 horas.<br>
        </p><br>

        <h2>Preparo da Cobertura</h2>
        <p>Na panela coloque :<br>
           a polpa do maracujá,<br>
           o açúcar,<br>
           e a água.<br>
           Leve ao fogo baixo, mexendo de vez em quando até ganhar consistência.<br>
           Retire do fogo, deixe amornar e espalhe sobre a mousse.<br>
           Leve à geladeira por cerca de 2 horas para gelar.<br>
           Sirva gelado.<br>
           Rendimento: 8 porções.<br>
        </p><br>
