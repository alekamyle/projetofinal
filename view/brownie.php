        <h2>Receita</h2>

        <h2> Ingredientes </h2>
        <p>65 gramas de cacau em pó sem açúcar (use o melhor cacau em pó).<br>
        150 gramas de óleo vegetal ou óleo de cozinha.<br>
        3 ovos médios e água morna suficiente para demolhar.<br>
        300 gramas de açúcar (ajuste de acordo com o seu gosto, se reduzir a quantidade de açúcar a superfície pode não ficar brilhante).<br>
        85 gramas de farinha de trigo.<br>
        Material suplementar.<br>
        3/4 colher de chá de sal.<br>
        1 colher de chá de baunilha.<br>
        Importante<br>
        Quanto mais você bater e o açúcar se dissolver, mais fácil será para você obter uma superfície brilhante</p><br>

