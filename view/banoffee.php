    <section class="receita">
        <h2>Banoffee</h2>

        <h2> Ingredientes da massa </h2>
        <p>300g de biscoito tipo maizena<br>
            150g de manteiga ou margarina<br>
        </p>

        <h2> Creme</h2>
        <p> 500 ml de leite<br>
            2 colheres (sopa) de amido de milho<br>
            2 gemas passadas pela peneira<br>
            1 lata de doce de leite<br>
             </p>


        <h2>Ganache de chocolate</h2>
        <p>100g de chocolate meio amargo<br>
          ½ caixa de creme de leite<br>
          4 bananas tipo nanica<br>
        </p>

        <h2>Ingredientes da cobertura</h2>
        <p>200 ml de chantilly batido<br>
           chocolate em pó ou canela em pó para polvilhar a gosto<br>
        </p>

        <h2>Preparo do Creme</h2>
        <p>Na panela coloque o leite e dissolva bem o amido de milho.<br>
           unte as gemas passadas pela peneira e o doce de leite.<br>
           Leve ao fogo baixo mexendo sem parar até engrossar.<br>
           Retire do fogo, transfira para uma vasilha e cubra com um plástico em contato e deixe esfriar. <br>
        </p>

        <h2>Preparo da Base da Torta</h2>
        <p>No processador ou liquidificador coloque os biscoitos e bata bem até formar uma farofinha.<br>
           Junte a manteiga ou margarina derretida e bata mais um pouco.<br>
           Despeje sobre uma forma de fundo falso de 25 cm de diâmetro.<br>
           E com o auxilio de uma colher modele a torta dando uma leve pressionada na base e na lateral da forma.<br>
           Leve ao forno preaquecido a 180ºC por cerca de uns 15 minutos.<br>
           Retire do forno e deixe esfriar.<br>
        </p>

        <h2>Montagem</h2>
        <p>Sobre a base da torta já fria despeje toda a ganache espalhando bem.<br>
           Em cima da ganhache coloque todo o creme alisando bem sobre a torta.<br>
           Leve para a geladeira e reserve.<br>
           Corte as bananas maduras em rodelas e acomode em cima da torta preenchendo bem.<br>
           Desenforme a torta e reserve.<br>
           Na batedeira coloque o chantilly bem gelado e bata até ficar firme.<br>
           Decore a torta com o chantilly e polvilhe chocolate em pó ou canela em pó a gosto.<br>
        </p>

